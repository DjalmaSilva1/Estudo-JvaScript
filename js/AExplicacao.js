// EXplicação sobre os operadores matematicos 
Math.abs(num) //Retorna o valor absoluto de um numero. se o valor for negativo vai retonar positivo

Math.ceil(num)// Arredonda o valor para cima

Math.floor(num)// Arredonda o valor para baixo

Math.pow(num)// Retorna a base elevada ao expoent
//exemplo : Math.pow(3, 2) =>9

Math.random(num)// Retorna um numero aleatorio entre 0 e 1, com varias casas decimais

Math.round(num)// Arredonda o valor para o inteiro mais proximo

Math.sqrt(num)// Retorna a riz quadrada de um numero